// Fonction pour obtenir une question aléatoire selon la catégorie et la difficulté
export function getRandomQuestion(category, difficulty = 'medium') {
  const questions = QUESTIONS_DATABASE[category]?.[difficulty];
  if (!questions || questions.length === 0) {
    return "Question non disponible pour cette catégorie et difficulté.";
  }
  return questions[Math.floor(Math.random() * questions.length)];
}

// Fonction pour obtenir toutes les questions d'une catégorie
export function getAllQuestions(category) {
  const categoryQuestions = QUESTIONS_DATABASE[category];
  if (!categoryQuestions) return [];
  
  return [
    ...categoryQuestions.easy,
    ...categoryQuestions.medium,
    ...categoryQuestions.hard
  ];
}

// Fonction pour obtenir le nombre total de questions
export function getTotalQuestionsCount() {
  let total = 0;
  Object.values(QUESTIONS_DATABASE).forEach(category => {
    Object.values(category).forEach(difficulty => {
      total += difficulty.length;
    });
  });
  return total;
}