// Fonction pour obtenir un gage aléatoire selon l'âge et la difficulté
export function getRandomGage(ageGroup, difficulty = 'medium') {
  const gages = GAGES_DATABASE[ageGroup]?.[difficulty];
  if (!gages || gages.length === 0) {
    return "Gage non disponible pour cette tranche d'âge et difficulté.";
  }
  return gages[Math.floor(Math.random() * gages.length)];
}

// Fonction pour obtenir tous les gages d'une tranche d'âge
export function getAllGages(ageGroup) {
  const ageGages = GAGES_DATABASE[ageGroup];
  if (!ageGages) return [];
  
  return [
    ...ageGages.easy,
    ...ageGages.medium,
    ...ageGages.hard
  ];
}

// Fonction pour obtenir le nombre total de gages
export function getTotalGagesCount() {
  let total = 0;
  Object.values(GAGES_DATABASE).forEach(ageGroup => {
    Object.values(ageGroup).forEach(difficulty => {
      total += difficulty.length;
    });
  });
  return total;
}

// Fonction pour déterminer la tranche d'âge à partir du profil familial
export function getAgeGroupFromProfile(profile) {
  const ageMapping = {
    'young': 'young',
    'mixed': 'mixed', 
    'teen': 'teen',
    'adult': 'adult'
  };
  return ageMapping[profile] || 'mixed';
}